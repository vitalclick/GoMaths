#!/usr/bin/env node
/**
 * Postinstall: read `node_modules/katex/dist/katex.min.css` and emit it
 * as `apps/student/lib/katex-css.bundle.ts` so the WebView + iframe
 * templates can inline the stylesheet (offline-safe, no CDN dependency).
 *
 * Idempotent — re-runnable on every `pnpm install`. The output file is
 * gitignored; `katex-css.ts` (committed) does a try/require of this
 * generated module and falls back to "" (which triggers a CDN link) if
 * the bundle isn't present.
 */

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const cssPath = resolve(here, "..", "node_modules", "katex", "dist", "katex.min.css");
const outPath = resolve(here, "..", "lib", "katex-css.bundle.ts");

const header = "// Auto-generated by scripts/bundle-katex-css.mjs. Do not edit.\n";

mkdirSync(dirname(outPath), { recursive: true });

try {
  const css = readFileSync(cssPath, "utf8");
  const escaped = JSON.stringify(css);
  writeFileSync(outPath, `${header}export const KATEX_CSS = ${escaped};\n`);
  console.log(`✓ Bundled katex.min.css (${css.length} chars) → ${outPath}`);
} catch (err) {
  console.warn(
    `! Could not bundle katex CSS (${err.message}). App will use CDN fallback for math rendering.`,
  );
}
